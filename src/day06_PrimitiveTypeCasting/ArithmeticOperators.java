package day06_PrimitiveTypeCasting;

public class ArithmeticOperators {

    public static void main(String[] args) {

        System.out.println(100 / 4);
        System.out.println(100.0 / 4);

        System.out.println(100D);

        /*

        iinteger/ integer === int
        deci/int === deci
        int/deci === deci
        deci/deci === deci

         */

    }
}
